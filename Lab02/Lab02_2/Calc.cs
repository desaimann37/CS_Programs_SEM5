using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Lab02_2
{
    public class Calc
    {
        public int Add(int a , int b)
        {
            return a + b;
        }
        public int Sub(int a , int b)
        {
            return a - b;
        }
        public int Mul(int a , int b)
        {
            return a * b;
        }
        public double Div(double a , double b)
        {
            return a / b;
        }
    }
}
